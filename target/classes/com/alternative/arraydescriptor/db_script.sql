/
-- ======================================================
-- Create array type of created row type object
-- ======================================================
CREATE OR REPLACE TYPE  SCHM001.TYPE_ROW_ARR AS VARRAY(5000) OF SCHM001.TYPE_ROW_OBJ;
/
-- ======================================================
-- Create Procedure
-- ======================================================
CREATE OR REPLACE PROCEDURE proc_name (
	I_CURSOR IN TYPE_ROW_ARR
    ) AS 
    --  Local Variables:
    LV_PROC_NAME  VARCHAR2(50)  :=  'proc_name';
    
BEGIN
    FOR LV_IDX IN 1..I_CURSOR.COUNT 
    LOOP
        DBMS_OUTPUT.PUT_LINE(LV_PROC_NAME,'Record#'||LV_IDX||': '|| I_CURSOR(LV_IDX).ROW_ATTRIBUTE01
            ||' - '||I_CURSOR(LV_IDX).ROW_ATTRIBUTE02
            ||' - '||I_CURSOR(LV_IDX).ROW_ATTRIBUTE03
            ||' - '||I_CURSOR(LV_IDX).ROW_ATTRIBUTE04,LV_LOGGED_BY);
    END LOOP;
    
EXCEPTION
    WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE(SQLERRM);
END POST_USER_ACCESS_CHANGES;