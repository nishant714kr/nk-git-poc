,	ROW_ATTRIBUTE07	VARCHAR2 (100)
,	ROW_ATTRIBUTE08	VARCHAR2 (100)
,	ROW_ATTRIBUTE09	VARCHAR2 (100)
,	ROW_ATTRIBUTE10	VARCHAR2 (100)
,	ROW_ATTRIBUTE11	VARCHAR2 (255)
,	ROW_ATTRIBUTE12	VARCHAR2 (255)
,	ROW_ATTRIBUTE13	VARCHAR2 (255)
,	ROW_ATTRIBUTE14	VARCHAR2 (255)
,	ROW_ATTRIBUTE15	VARCHAR2 (255)
